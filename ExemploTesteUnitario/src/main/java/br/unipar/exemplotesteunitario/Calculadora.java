package br.unipar.exemplotesteunitario;

/**
 *
 * @author Antony
 */
public class Calculadora {

    public int soma(int val1, int val2) {
        return val1+val2;
    }

    public int subtracao(int val1, int val2) {
        return val1-val2;
    }

    public double multiplicacao(double val1, double val2) {
        return val1*val2;
    }

    public double divisao(double val1, double val2) {
        return val1/val2;
    }
}
